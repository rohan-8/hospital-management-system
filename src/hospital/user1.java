    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:hhhh
        String name = txtname.getText();
        String username = txtusername.getText();
        String password = txtpassword.getText();
        String usertype = txtutype.getSelectedItem().toString();
        
        Connect();
        try {
            pat = con.prepareStatement("insert into user (name,username,password,utype)values(?,?,?,?)");
            pat.setString(1, name);
            pat.setString(2, username);
            pat.setString(3, password);
            pat.setString(4, usertype);
            
            pat.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "User Inserted Successfully!!");
            
            txtname.setText("");
            txtusername.setText("");
            txtpassword.setText("");
            txtutype.setSelectedIndex(-1);
            txtname.requestFocus();
            new Doctor1(username,password).setVisible(true);
            
        } catch (SQLException ex) {
            Logger.getLogger(user1.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

